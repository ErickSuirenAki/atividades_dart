void main() {
  String diaDaSemana = 'segunda';

  switch (diaDaSemana) {
    case 'segunda':
      print('Hoje é segunda-feira');
      break;
    case 'terça':
      print('Hoje é terça-feira');
      break;
    case 'quarta':
      print('Hoje é quarta-feira');
      break;
    case 'quinta':
      print('Hoje é quinta-feira');
      break;
    case 'sexta':
      print('Hoje é sexta-feira');
      break;
    default:
      print('É um fim de semana!');
  }
}
